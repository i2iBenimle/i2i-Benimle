package i2i_benimle;

public class Wallet {
	int id;
	int amount;
	int customerId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getCustomer_id() {
		return customerId;
	}
	public void setCustomer_id(int customer_id) {
		this.customerId = customer_id;
	}	
}
